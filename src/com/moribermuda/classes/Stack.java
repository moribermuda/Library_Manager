public class Stack
{
    private int stack_id;
    private String stack_Titel;
    private int rowCount;
    private int columnCount;

    public int getRowCount()
    {
        return rowCount;
    }

    public void setRowCount(int rowCount)
    {
        this.rowCount = rowCount;
    }

    public int getColumnCount()
    {
        return columnCount;
    }

    public void setColumnCount(int columnCount)
    {
        this.columnCount = columnCount;
    }
     
    public int getStack_id()
    {
        return stack_id;
    }

    public void setStack_id(int stack_id)
    {
        this.stack_id = stack_id;
    }

    public String getStack_Titel()
    {
        return stack_Titel;
    }

    public void setStack_Titel(String stack_Titel)
    {
        this.stack_Titel = stack_Titel;
    }

  
    
}
